--STORED PROCEDURE PARA A INSERSï¿½O DE MISSï¿½ES ESPACIAIS LOGISTICAS NA TABELA 'SPACE_LOGISTICS'
CREATE PROCEDURE SP_INSERT_SPACE_LOGISTICS
@MISSION_NAME varchar(100),
@MISSION_TYPE_ID int,
@MISSION_COMPANY_ID int,
@MISSION_TRANSPORT_VEHICLE_ID int,
@DESCRIPTION text,
@LAUNCH_DATE date,
@LANDING_DATE date,
@LAUNCH_LOCATION varchar(50),
@LANDING_LOCATION varchar(50),
@BUDGET float
AS
BEGIN
	INSERT INTO OPERATION_LOGS(OPERATION_NAME, OPERATION_CATEGORY_ID, OPERATION_STATUS_ID) VALUES('SP_LG-' + @MISSION_NAME, 1, 1) --INSERSï¿½O DA OPERAï¿½ï¿½O A QUE ESTA MISSï¿½O ESTï¿½ ASSOCIADA (REFERENCIA NA TABELA OPERATION_LOGS)
	DECLARE @LAST_OPERATION_ID INT;
	SET	@LAST_OPERATION_ID = (SELECT MAX(OPERATION_LOGS.OPERATION_ID) FROM OPERATION_LOGS);

	--INSERSï¿½O DA MISSï¿½O NA TABELA SPACE_LOGISTICS
	INSERT INTO SPACE_LOGISTICS(OPERATION_ID, MISSION_NAME, MISSION_TYPE_ID, MISSION_COMPANY_ID, MISSION_TRANSPORT_VEHICLE_ID, DESCRIPTION, LAUNCH_DATE, LANDING_DATE, LAUNCH_LOCATION, LANDING_LOCATION, BUDGET) VALUES(@LAST_OPERATION_ID,
	@MISSION_NAME,
	@MISSION_TYPE_ID,
	@MISSION_COMPANY_ID,
	@MISSION_TRANSPORT_VEHICLE_ID,
	@DESCRIPTION,
	@LAUNCH_DATE,
	@LANDING_DATE,
	@LAUNCH_LOCATION,
	@LANDING_LOCATION,
	@BUDGET)
END

GO

