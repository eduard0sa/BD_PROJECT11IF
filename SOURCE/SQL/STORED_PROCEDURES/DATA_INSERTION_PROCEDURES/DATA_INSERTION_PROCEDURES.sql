--PROCEDURE PARA INSERSï¿½O DE CENTRAL ELï¿½TRICA NA TABELA 'POWER_PLANTS'. DURANTE ESSA INSERSï¿½O, ESTA ï¿½ TAMBï¿½M ASSOCIADA ï¿½(S) CIDADE(S) ALIMENTADA(S) POR SI
CREATE PROCEDURE SP_INSERT_POWER_PLANTS
@POWER_PLANT_NAME varchar(50),
@POWER_PLANT_COMPANY_ID int,
@POWER_PLANT_TYPE_ID int,
@POWER_PLANT_LOCATION varchar(100),
@POWER_PLANT_MAX_OUTPUT_CAPACITY float,
@POWER_PLANT_STATUS_ID int,
@POWER_PLANT_NEXT_MAINTENANCE_DATE date,
@POWER_PLANT_INSTALLATION_DATE date,
@TARGET_CITIES_ID varchar(20)
AS
BEGIN
	--=======================================================================================================================================================================================================================
	--INSERSï¿½O DA CENTRAL ELï¿½TRICA NA TABELA POWER_PLANTS
	INSERT INTO POWER_PLANTS(POWER_PLANT_NAME, POWER_PLANT_COMPANY_ID, POWER_PLANT_TYPE_ID, POWER_PLANT_LOCATION, POWER_PLANT_MAX_OUTPUT_CAPACITY, POWER_PLANT_STATUS_ID, POWER_PLANT_NEXT_MAINTENANCE_DATE, POWER_PLANT_INSTALLATION_DATE) VALUES(
	@POWER_PLANT_NAME,
	@POWER_PLANT_COMPANY_ID,
	@POWER_PLANT_TYPE_ID,
	@POWER_PLANT_LOCATION,
	@POWER_PLANT_MAX_OUTPUT_CAPACITY,
	@POWER_PLANT_STATUS_ID,
	@POWER_PLANT_NEXT_MAINTENANCE_DATE,
	@POWER_PLANT_INSTALLATION_DATE)

	--=======================================================================================================================================================================================================================

	DECLARE @_POWER_PLANT_ID INT; --ID DA CENTRAL ELï¿½TRICA INSERIDA
	SET @_POWER_PLANT_ID = (SELECT MAX(POWER_PLANT_ID) FROM POWER_PLANTS) --A CENTRAL INSERIDA VAI TER O MAIOR ID DA TABELA (ï¿½ A MAIS RECENTE)

	DECLARE @TARGET_CITIES_TEMP_TABLE TABLE(splited_str varchar(4)) --TABELA TEMPORARIA PARA GUARDAR OS ID's DAS CIDADES A CONECTAR ï¿½ CENTRAL
	INSERT INTO @TARGET_CITIES_TEMP_TABLE(splited_str)	(SELECT [value] FROM STRING_SPLIT(@TARGET_CITIES_ID, ',')); --CONVERSï¿½O DE VARCHAR(20) PARA TABLE (SPLIT DA STRING)

	DECLARE @I INT = 1; -- CONTADOR PARA CICLO WHILE
	WHILE @I-1 < (SELECT COUNT(*) FROM @TARGET_CITIES_TEMP_TABLE) -- ESTE CICLO VAI PERCORRER TODOS OS VALORES DA TABELA @TARGET_CITIES_TEMP_TABLE, ENQUANTO @I-1 FOR MENOR QUE O Nï¿½MERO DE REGISTOS DESTA
	BEGIN
		DECLARE @_CITY_ID INT; --ID DA CIDADE A LIGAR ï¿½ CENTRAL ELï¿½TRICA
		SET @_CITY_ID = (SELECT TOP(1) CONVERT(INT, splited_str) FROM @TARGET_CITIES_TEMP_TABLE WHERE @I = CONVERT(INT, splited_str))

		INSERT INTO CITY_REL_POWERPLANTS(CITY_ID, CITY_ENERGY_SOURCE_ID) VALUES(@_CITY_ID, @_POWER_PLANT_ID) --INSERSï¿½O DA LIGAï¿½ï¿½O DA CIDADE COM A CENTRAL ELï¿½TRICA, NA TABELA QUE AS RELACIONA (CITY_REL_POWERPLANTS)
		SET @I = @I + 1 --INCREMENTO DO CONTADOR @I
	END
	--=======================================================================================================================================================================================================================
END
/*EXEMPLO DE EXECUï¿½ï¿½O:
