    @USER_ACADEMIC_LEVEL_ID INT,
    @USER_JOB_ID INT,
    @USER_LEVEL_ID INT,
	@USER_EMPLOYER_COMPANY_ID INT
AS
BEGIN
	INSERT INTO USERS (
    USER_FIRST_NAME,
    USER_LAST_NAME,
    USER_PASSWORD_HASH,
    USER_EMAIL,
    USER_PHONENUMBER,
    USER_PROFILE_PICTURE_PATH,
    USER_BIRTHDATE,
    USER_GENDER_ID,
    USER_COUNTRY_ID,
    USER_ADRESS,
    USER_CITIZEN_CARD_NUMBER,
    USER_SALARY,
    USER_BANK_ACCOUNT_NUMBER,
    USER_ACADEMIC_LEVEL_ID,
    USER_JOB_ID,
    USER_LEVEL_ID
	) VALUES
	(@USER_FIRST_NAME,
	@USER_LAST_NAME,
	@USER_PASSWORD_HASH,
	@USER_EMAIL,
	@USER_PHONENUMBER,
	@USER_PROFILE_PICTURE_PATH,
	@USER_BIRTHDATE,
	@USER_GENDER_ID,
	@USER_COUNTRY_ID,
	@USER_ADRESS,
	@USER_CITIZEN_CARD_NUMBER,
	@USER_SALARY,
	@USER_BANK_ACCOUNT_NUMBER,
	@USER_ACADEMIC_LEVEL_ID,
	@USER_JOB_ID,
	@USER_LEVEL_ID);

	DECLARE @USER_ID INT;
	SET @USER_ID = (SELECT MAX(USER_ID) FROM USERS (NOLOCK))

	INSERT INTO USER_REL_COMPANY (USER_COMPANY_ID, USER_ID)
	VALUES (@USER_EMPLOYER_COMPANY_ID, @USER_ID)
END